-- 1. 部署マスタ
CREATE TABLE IF NOT EXISTS departments_mst (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dept_code VARCHAR(50) NOT NULL UNIQUE,
    dept_name VARCHAR(50) NOT NULL,
    created_by VARCHAR(50) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_by VARCHAR(50) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL
);

-- 2. 権限マスタ
CREATE TABLE IF NOT EXISTS roles_mst (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_code VARCHAR(20) NOT NULL UNIQUE,
    role_name VARCHAR(50) NOT NULL,
    created_by VARCHAR(50) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_by VARCHAR(50) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL
);

-- 3. 勤怠状況マスタ
CREATE TABLE IF NOT EXISTS statuses_mst (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    status_code VARCHAR(50) NOT NULL UNIQUE,
    status_name VARCHAR(50) NOT NULL,
    created_by VARCHAR(50) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_by VARCHAR(50) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL
);

-- 4. 従業員マスタ
CREATE TABLE IF NOT EXISTS employees_mst (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_code VARCHAR(20) NOT NULL UNIQUE,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    password VARCHAR(255) NOT NULL,
    department_id BIGINT REFERENCES departments_mst(id),
    role_id BIGINT NOT NULL REFERENCES roles_mst(id),
    joined_date DATE,
    version INTEGER NOT NULL DEFAULT 1,
    created_by VARCHAR(50) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_by VARCHAR(50) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL
);

-- 5. 勤怠記録
CREATE TABLE IF NOT EXISTS attendance_records (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    employee_id BIGINT NOT NULL REFERENCES employees_mst(id),
    work_date DATE NOT NULL,
    clock_in_time TIMESTAMP WITH TIME ZONE,
    clock_out_time TIMESTAMP WITH TIME ZONE,
    status_id BIGINT NOT NULL REFERENCES statuses_mst(id),
    note VARCHAR(255),
    version INTEGER NOT NULL DEFAULT 1,
    created_by VARCHAR(50) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_by VARCHAR(50) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    CONSTRAINT uq_attendance_employee_date UNIQUE (employee_id, work_date)
);

-- 6. 休憩記録
CREATE TABLE IF NOT EXISTS break_records (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    attendance_id BIGINT NOT NULL REFERENCES attendance_records(id),
    break_start TIMESTAMP WITH TIME ZONE NOT NULL,
    break_end TIMESTAMP WITH TIME ZONE,
    version INTEGER NOT NULL DEFAULT 1,
    created_by VARCHAR(50) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_by VARCHAR(50) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL
);

-- 7. 外出記録
CREATE TABLE IF NOT EXISTS outing_records (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    attendance_id BIGINT NOT NULL REFERENCES attendance_records(id),
    outing_start TIMESTAMP WITH TIME ZONE NOT NULL,
    outing_end TIMESTAMP WITH TIME ZONE,
    version INTEGER NOT NULL DEFAULT 1,
    created_by VARCHAR(50) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_by VARCHAR(50) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL
);

-- セッション本体テーブル
CREATE TABLE IF NOT EXISTS spring_session (
    PRIMARY_ID CHAR(36) NOT NULL PRIMARY KEY,
    SESSION_ID CHAR(36) NOT NULL,
    CREATION_TIME BIGINT NOT NULL,
    LAST_ACCESS_TIME BIGINT NOT NULL,
    MAX_INACTIVE_INTERVAL INT NOT NULL,
    EXPIRY_TIME BIGINT NOT NULL,
    PRINCIPAL_NAME VARCHAR(100),
    CONSTRAINT spring_session_ix1 UNIQUE (SESSION_ID)
);

-- セッション属性テーブル
CREATE TABLE IF NOT EXISTS spring_session_attributes (
    SESSION_PRIMARY_ID CHAR(36) NOT NULL,
    ATTRIBUTE_NAME VARCHAR(200) NOT NULL,
    -- H2用に LONGVARBINARY を使用（前回の型不整合対策）
    ATTRIBUTE_BYTES LONGVARBINARY NOT NULL, 
    PRIMARY KEY (SESSION_PRIMARY_ID, ATTRIBUTE_NAME),
    CONSTRAINT spring_session_attributes_fk FOREIGN KEY (SESSION_PRIMARY_ID) 
        REFERENCES spring_session(PRIMARY_ID) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS spring_session_ix1 ON spring_session (LAST_ACCESS_TIME);
CREATE INDEX IF NOT EXISTS spring_session_ix2 ON spring_session (EXPIRY_TIME);
CREATE INDEX IF NOT EXISTS spring_session_ix3 ON spring_session (PRINCIPAL_NAME);